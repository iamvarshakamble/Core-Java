package com.xworkz.diamond;

public class Mine {
	private String name;
	private String work;
	
	public void setName(String name) {
		this.name = name;
	}
	public void setWork(String work) {
		this.work = work;
	}
	public String getName() {
		return name;
	}
	public String getWork() {
		return work;
	}

}

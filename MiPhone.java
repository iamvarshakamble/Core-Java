class MiPhone{
	
	String brand;
	double price;
	String color;
	int imei;
	PhonePay1 phonePay;
	
	void setBrand(String brand){
		this.brand=brand;
	}
	
	void setPrice(double price){
		this.price=price;
	}
	
	void setColor(String color){
		this.color=color;
	}
	
	void setImei(int imei){
		this.imei=imei;
	}
	
	void setPhonepay(PhonePay1 phonePay){
		this.phonePay=phonePay;
	}
}
class Curtain{
	private String brand;
	private String color;
	private String length;
	private boolean staning;
	private String type;
	
	void setBrand(String brand){
		this.brand=brand;
	}
	
	String getBrand(){
		return this.brand;
	}
	
	void setColor(String color){
		this.color=color;
	}
	
	String getColor(){
		return this.color;
	}
	
	void setLength(String length){
		this.length=length;
	}
	
	String getLength(){
		return this.length;
	}
	
	void setStaning(boolean staning){
		this.staning=staning;
	}
	
	boolean getStaning(){
		return this.staning;
	}
	
	void setType(String type){
		this.type=type;
	}
	
	String getType(){
		return this.type;
	}
}